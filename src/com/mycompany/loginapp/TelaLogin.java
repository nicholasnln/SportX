    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        
        String cpf = campoNomeUsuario.getText(); // Estou utilizando o campo de login para o CPF
        String senha = new String(campoSenha.getPassword());

        if (atletaService.login(cpf, senha)) {
            JOptionPane.showMessageDialog(this, "Login realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            // Abrir a tela de cadastro
            new TelaInicio().setVisible(true);
            // Fechar a tela de login atual
            this.dispose();
            //this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usu√°rio ou senha incorretos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed

